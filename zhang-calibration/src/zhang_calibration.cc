    Eigen::Matrix<double, 6, 1> a1, a2;

    double h1 = H(0, 0);
    double h2 = H(0, 1);
    double h3 = H(0, 2);
    double h4 = H(1, 0);
    double h5 = H(1, 1);
    double h6 = H(1, 2);
